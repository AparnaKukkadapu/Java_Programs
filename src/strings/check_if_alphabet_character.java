public class alphabet_character {    
    
    public static void main(String[] args) {
     
        char ch='T';

        if(ch>='a' && ch<='z'||ch>='A' && ch<='Z')
        {
            System.out.println(ch +" character is a alphabet");
        }
        else
         {
            System.out.println(ch +" character is not a alphabet");
        }
    }
}
